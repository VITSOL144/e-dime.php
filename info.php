<h4 id='stop'>Виды торможений</h4>

<p>Торможение полуплугом практикуется при спусках зигзагом. Для этого запятник лыжи, находящейся ниже по склону, отводится в сторону, а лыжа ставится на внутреннее ребро; вторая лыжа скользит в направлении движения.</p>
<p>Торможение плугом используется на склонах средней и малой крутизны при спусках по прямой: удерживая носки лыж вместе, развести пятки лыж в стороны и поставить лыжи на внутренние ребра, что достигается небольшим сближением коленей и переносом веса тела на пятки.</p>
<p>Торможение с помощью палок применяется при спусках зигзагом или по прямой. Для этого надо сложенные вместе палки опустить острием в снег и усиливая надавливание на них, замедлить движение.<span style='color:#2a52bf;'> Только учитывайте, что это будет последнее торможение в вашей жизни!</span></p>
<p>Торможение падением применяется в экстренных случаях, когда необходимо быстро остановиться. Удерживая палки остриями назад, быстро присесть и упасть на бок.<span style='color:#2a52bf;'> Довольно популярно. Жаль, что не у норвежцев.</span></p>
<span 
style='color:#2a52bf;'>А можете еще въехать в соперника на финише как Большунов. А потом сослаться на плохие крепления.</span>

