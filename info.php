<h4 id='turn'>Повороты на лыжах</h4>

<p>Повороты на лыжах в движении производятся переступанием, из положения плуга и из положения полуплуга.</p>
<p>Поворот переступанием используется на небольшой скорости на пологих склонах. Поворот из положения плуга обычно применяется после торможения при спуске по прямой. Поворот из положения полуплуга выполняется на более высоких скоростях и крутых склонах при спуске зигзагом.</p>

