<h4 id='down'>Преодоление спуска на лыжах</h4>

<p>При спусках  на лыжах по прямой различают три  основных стойки – высокую, среднюю и низкую. Чем круче склон и выше скорость спуска, тем ниже должна быть стойка лыжника.</p>
<p>На крутых склонах спускаться следует зигзагом, поворачивая на ходу или после остановки.</p>
<p>Боковым  соскальзыванием обычно спускаются с крутых, недлинных склонов, не имеющих препятствий (кочки, пни, деревья). Для этого необходимо поставить лыжи поперек склона, и соскальзывать вниз, упираясь на ребра лыж.</p>
<p>Спуск лесенкой применяется на таких же склонах, но имеющих препятствия, исключающие спуск зигзагом и боковое соскальзывание.</p>

