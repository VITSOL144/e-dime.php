<h4 id='up'>Способы подъёмов на лыжах</h4>

<p>В зависимости  от крутизны и протяженности склона, характера снежного покрова и подготовленности лыжника подъем можно осуществлять ступающим шагом, полуелочкой, елочкой, лесенкой.</p>
<p>Ступающим шагом поднимаются на ровных и пологих склонах. При этом туловище следует наклонить чуть больше вперед, а руками сильнее опираться на палки. При «отдаче» лыжи нужно ставить на снег с прихлопыванием.</p>
<p>Полуелочкой поднимаются на более крутых склонах. Одна лыжа ставится в направлении движения, а другая развернута носком в сторону и упирается в снег внутренним ребром.</p>
<p>Подъем елочкой применяется на склонах средней крутизны. Носки лыж широко разводятся в стороны и лыжи жестко ставятся на внутренние ребра с поочередной опорой на палки, которые втыкаются в снег позади лыж.</p>
<p>Лесенкой поднимаются на крутых и обрывистых склонах. Стоя боком к склону, последовательно переставлять вверх лыжи и палки. На склонах с твердым снежным покровом лыжи нужно ставить с прихлопыванием на ребро.</p>
<p>Также в горку можно подниматься коньковым ходом, попеременным двушажным, а также методом Йоханнеса Клебо. Эти способы требуют высокой физической подготовки</p>

