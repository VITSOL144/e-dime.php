<p><b>Лондонский затяжной</b> - типичный прокат чайника</p>
<p><b>"Жить осталось два понедельника"</b> - причина нежелания переучиваться с классики на конек</p>
<p><b>Не попасть в смазку</b> - типичная отмазка проигравшего</p>
<p><b>В одно рыло</b> - уйти в отрыв и лидировать до конца гонки или тренировка, выполняемая в одиночку</p>
<p><b>Курить</b> - ехать, не напрягаясь</p>
<p><b>Искать себя на коленях</b> - т.е. внизу протокола</p>
<p><b>Метать фарш</b> - плохо чувствовать себя на дистанции</p>
<p><b>Тягун</b> - длинный подъем с равномерной крутизной</p>
<p><b>Торчок</b> - очень короткий и крутой подъем, который обычно запрыгивается "елочкой"</p>
<p><b>Деревянная медаль</b> - медаль за 4 место</p>
<p><b>Бросить лыжные гонки</b> - совет чайнику</p>

<p><b><i>Лыжные гонки - ваш путь к победе! Будьте счастливы!</i></b></p>
