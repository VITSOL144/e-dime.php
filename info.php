<h3 id='prepare'>Обработка лыж</h3>

<p>Все знают, что лыжи чем-то мажут. Однако на лицах обывателей возникает удивление, когда они слышат, что лыжи мажут не для того, чтобы они лучше ехали, а совсем наоборот. Дело в том, что для разных стилей катания требуется разная подготовка. Рассмотрим оба варианта и разберемся, в чем причина недоумения.</p>

<h4 id='class'>Под классику</h4>

<p>Передвигаясь классическим ходом, лыжник двигается по лыжне, толкаясь ногами поочередно. Из-за гладкой скользящей поверхности получается проскальзывание лыжи и толкание становится неэффективным. </p>
<p>Чтобы этого избежать, центральная часть лыжи - колодка смазывается мазью и растирается пробкой. После нанесения мази и полировки, дайте лыже постоять на воздухе, после чего приступайте к катанию.</p>
<p>Учтите, что мазь следует подбирать согласно температуре воздуха за окном. После катания снимите мазь с помощью скребка и смывки (скипидара), протрите лыжу чистой тряпочкой.</p>

<figure>
	<img src='info/classic_serve.jpg'>
	<figcaption>Лыжная мазь</figcaption>
</figure>

<h4 id='skate'>Под конёк</h4>

<p>Чтобы грамотно подготовить лыжи под конек, вам придется постараться. Для начала, с помощью смывки и тряпочки, очистите лыжу от грязи и прошлой мази или парафина. Затем нанесите парафин тонким слоем на лыжу (парафин подбирается по температуре). Разровняйте его утюгом, или феном с пробкой (хотя если парафина немного, можно растереть одной пробкой). Дайте лыже остыть.</p>
<p>Снимите лишний слой парафина циклей или скребком. Затем отполируйте скользящую поверхность бронзовой и нейлоновой щетками. Смахните грязь тряпочкой и поморозьте минут 10. Лыжи готовы.</p> 
<p>Теперь самое интересное. Вышеуказанная подготовка предназначена для профессиональных лыжников-гонщиков. В их лексиконе есть еще такие умные слова, как штайн-шлифт, конский волос, фибертекс, накатка структуры, порошки и т.д. Для лыжников уровня среднего и ниже среднего (которые составляют большинство) достаточно будет парафинить перед соревнованиями и при резких изменениях погоды. Перед тренировками проходитесь бронзовой и нейлоновыми щетками и - вперед!</p>

