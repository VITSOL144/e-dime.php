</figure>

<p>В 1918 году в России (СССР) лыжный спорт был включен в обязательную программу в спортивных вузах всей нашей страны.</p>

<p>Советские спортсмены, которые побеждали на отечественных чемпионатах, достойно выступали и на международных соревнованиях.</p>

