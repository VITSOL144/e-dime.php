	<h4 id='hod'>Виды лыжных ходов, их краткая  характеристика</h4>
<b><i>Лыжные ходы бывают классическими и коньковыми!</i></b>

<p>В коньковом ходе цикл состоит из двух скользящих шагов и включает две фазы: свободное одноопорное скольжение и скольжение с отталкиванием ногой, в процессе которых лыжник дважды одновременно отталкивается руками.</p>
<figure>
	<img src='info/skate.jpg'>
	<figcaption>Коньковый ход</figcaption>
</figure>
<p>Классические  лыжные ходы по работе ног подразделяются на бесшажные, одношажные и двухшажные. По работе рук – на одновременные и попеременные.</p>
<figure>
	<img src='info/classic.jpg'>
	<figcaption>Классический ход</figcaption>
</figure>
<b>Одновременный бесшажный ход</b>
<p>Передвижение  осуществляется только за счет одновременного отталкивания палками. Применяется ход на пологих спусках и на равнине при хорошем скольжении. Цикл состоит из свободного скольжения на двух лыжах и одновременного отталкивания палками.</p>

<b>Одновременный одношажный ход</b>
<p>Применяется на равнине и на пологих подъемах при хорошем скольжении. Цикл состоит из одного отталкивания ногой, одновременного отталкивания руками и свободного скольжения на двух лыжах.</p>

<b>Попеременный двухшажный ход</b>
<p>Один из основных способов передвижения на лыжах, применяется на подъемах малой и средней крутизны, а также на равнине при плохом скольжении. Цикл состоит из двух скользящих шагов и попеременных отталкиваний палками на каждый шаг.</p>

<b>Даблполлинг</b>
<p>Это для сорвиголов. Тоже самое что бесшажный, только подпрыгиваем при замахе. Когда до финиша остается 100 м, а надо обогнать еще парочку норгов и финна - даблполлинг для вас!</p>
