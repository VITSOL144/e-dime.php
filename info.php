	<li><p>Если вы не послушали 1 совета и готовы к лошадиному труду, запомните: лыжник - это не хобби и не увлечения, это - состояние души. Вас ждут длительные и интервальные тренировки, мокрый и перемороженный снег, потеря желание жить на 36-м километре марафона где-нибудь в заснеженных горах Мурманской области. Вас ждет матерящийся тренер (если он у вас есть), едущий рядом на снегоходе. Однажды утром вы проснетесь с болью во всем теле, вы не сможете ответить себе на вопрос "За каким хреном я так надрываюсь?" и будете самым счастливым человеком на Земле. Наверное это того стоит.</p></li>
	<li><p>Выбирая, на какие соревы податься, ознакомьтесь со списком конкурентов. Не обольщайтесь их почтенным возрастом. Мужика 1950 года рождения (зная его опыт) вы в жизни не обгоните.</p></li>
	<li><p>Подходя на соревнованиях к линии старта вы должны чувствовать себя как космонавт перед полетом: ничто не должно вам мешать. </p></li>
	<li><p>Если за первые 100-200 м вас не собьют и не сломают палку, считайте гонка прошла удачно. </p></li>
	<li><p>Намного легче догонять, чем ехать впереди; грамотно будет найти свой темп, сесть кому-нибудь на хвост и работать так всю дистанцию, измотав его морально, а под конец пойти в атаку. </p></li>
	<li><p>Благодаря свойству "воздушного мешка", на спуске всегда выигрывает позади идущий, поэтому не рвите лошадей. </p></li>
	<li><p>Берите с собой наручные часы. </p></li>
	<li><p>Не позируйте перед фотографами на дистанции, а то въедете в дерево, кто тогда норгов обгонит?.. </p></li>
	<li><p>Какое бы место у вас не было финишируйте с хорошим настроением. </p></li>
	<li><p>Поблагодарите соперников. После финиша они - ваши друзья! </p></li>
</ol>

<h3 id='dictionary'>Терминология</h3>

<p><b>Лыжиться</b> - приставать, наглеть</p>
<p><b>Парафиниться</b> - заморачиваться</p>
<p><b>Один-тюлень, другого ветром сдувает</b> - речь идет о главных соперниках</p>
<p><b>Запарафинить лыжу</b> - найти объект интереса в женском коллективе</p>
<p><b>Лоб (он же лобовик)</b> - не очень длинный, но крутой подъем, который хорошо просматривается снизу до верху.</p>
<p><b>Рыбья кость</b> - длинный подъем с переменной крутизной.</p>
<p><b>Полка</b> - ровный участок посередине поъема.</p>
<p><b>Вздрючка</b> - резкий крутой участок, обычно, в конце трудного длинного подъема.</p>
<p><b>Стиральная доска</b> - валикообразные неровности поперек лыжни.</p>
<p><b>Целяк</b> - сильнопереметенная лыжня или пространство за пределами накатанной лыжни.</p>
<p><b>"Малиновый Закат"</b> - общее определение всех подъемов протяженностью свыше 1-1,5 км.</p>
<p><b>Прогон</b> - протяженный равнинный участок между двумя частями трассы с сильнопересеченным рельефом.</p>
<p><b>Чёпик вшторить</b> - выиграть с явным преимуществом</p>
<figure>
