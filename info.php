<h3 id='history'>История лыжного спорта</h3>

<p>Приспособления, которые привели к появлению современных лыж, появились очень давно, практически у всех известных истории древних народов, которые проживали в холодных заснеженных регионах нашей планеты.</p>

<p>Первые лыжи, которые изобрел человек, были очень просты, - это были ступающие лыжи - снегоступы. Одна из таких находок, которая была обнаружена на территории Псковской области (Россия), - это лыжа, которая по заключению специалистов, была сделана около 4300 лет назад. И является одной из самых древних среди найденных.</p>

<figure>
	<img src='info/drev.jpg'>
	<figcaption>Древние лыжи</figcaption>
</figure>

<p>Лыжи существенно изменились, когда человек научился распиливать деревья на доски, и изготавливать из досок деревянные лыжи, которые хорошо скользили по снегу.</p>

<p>Первые письменные документы, упоминающие об использование скользящих лыж, относятся к VI-VII векам нашей эры. Так, Готский монах Жорданес в 552 году, греческие историки Иордан в VI веке и  Авел Диакон в 770 году, описывают использование лапландцами и финнами лыж в быту и на охоте.</p>

<p>В конце VII века историк Верефрид дал подробное описание лыж и их использование народами Севера на охоте за зверем.</p>

<p>Король Норвегии Олаф Тругвассон по записям 925 года характеризовался как хороший лыжник.</p>

<p>Принято считать, что современный лыжный спорт начал формироваться в Норвегии. Так в 1733 году Ганс Эмахузен подготовил и издал первое наставление по лыжной подготовке войск с явно спортивным уклоном.</p>

<p>В 1767 году в армии Норвегии были проведены первые соревнования по всем видам лыжного спорта (по современным понятиям): биатлону, слалому, скоростному спуску и гонкам.</p>

<p>Первая в мире выставка различных типов лыж и лыжного инвентаря была открыта в Тронхейме, в 1862-1863 годах.</p>

<p>В 1877 оду в Норвегии организовано первое лыжное спортивное общество, вскоре в Финляндии открыли спортивный лыжный клуб. Затем лыжные клубы начали открываться и в других странах Европы, Азии и Америки.</p>

<p>Постепенно росла популярность лыжных праздников в Норвегии - Холменколленские игры (с 1883 года), Финляндии - Лахтинские игры (с 1922 года), Швеции - массовая лыжная гонка «Васалоппет» (с 1922 года).</p>

<p>В конце 19-го века соревнования по лыжному спорту стали проводиться во многих странах мира. Лыжная специализация в разных странах была различной. В Норвегии большое развитие получили гонки на пересеченной местности, прыжки и двоеборье. В Швеции - гонки на пересеченной местности. В Финляндии и России - гонки по равнинной местности. В США развитию лыжного спорта способствовали переселенцы из скандинавских стран. В Японии лыжный спорт под влиянием австрийских тренеров стал развиваться в горнолыжном направление.</p>

<p>В 1910 году в Осло состоялся международный лыжный конгресс с участием 10 стран. На нем была создана Международная лыжная комиссия, реорганизованная в 1924 году в Международную лыжную Федерацию.</p>

<p>В 1924 году состоялись первые зимние Олимпийские игры, включающие соревнования по лыжному спорту, в 1926 году - было проведено Первенство мира по лыжному спорту, а в 1928 году - Универсиада.</p>

 

<h4>Развитие лыжного спорта в России</h4>

<p>Во многом история появления лыжного спорта в России похожа на историю наших северных соседей - скандинавов.</p>

<p>Официально, бег на лыжах в России, превратился в спортивную дисциплину 29 декабря 1895 года. В этот день в Москве торжественно открылся «Московский клуб лыжников» - первая организация, которая на официальном уровне занималась всеми вопросами, связанными с зимними видами спорта.</p>

<p>В 1897 году в Петербурге открывается клуб лыжников «Полярная звезда», в которую вступили все любители лыж Северной столицы.</p>

<p>Затем, на протяжении нескольких лет и в других крупных городах России организовывались подобные лыжные клубы, которые существовали автономно.</p>

<p>К 1910 году лыжные клубы России приняли решение об объединение, в результате была создана Московская лига лыжебежцев. И в 1910 году, под руководством лиги, в зимний период в России, прошло рекордное количество лыжных соревнований - 18, в которых в общей сложности приняло участие более 100 человек.</p>

<p>Так, 7 февраля 1910 года была проведена лыжная гонка на 30 км, в которой приняли участие спортсмены из Москвы и Петербурга. Золото на этой лыжной гонке завоевал Павел Бычков - первый победитель по лыжному бегу в России.</p>

<figure>
	<img style='height:195px;width:195px;'src='info/pavel.jpg'>
